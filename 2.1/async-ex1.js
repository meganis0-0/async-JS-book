// ❌ СИНХРОННЫЙ ПОДХОД - блокирующий
console.log("Начало синхронной операции");
const start = Date.now();
while (Date.now() - start < 2000) {} // Блокировка на 2 секунды
console.log("Конец операции");
// ❌ АСИНХРОННЫЙ ПОДХОД - неблокирующий
console.log("Начало асинхронной операции");
setTimeout(() => {
  console.log("Асинхронная операция завершена");
}, 2000);
console.log("Программа продолжает работать");
